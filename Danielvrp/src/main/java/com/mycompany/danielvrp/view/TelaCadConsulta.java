    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try{
            if( edtConvenio.getText().trim().isEmpty()||cmbPaciente.getItemCount() == 0||cmbMedico.getItemCount() == 0)
                JOptionPane.showMessageDialog(rootPane, "PREENCHA TODOS OS CAMPOS");
            else{
                Consulta consulta = new Consulta();
                if(!edtId.getText().isEmpty())
                    consulta.setIdconsulta(Integer.parseInt(edtId.getText()));
                consulta.setConvenio(edtConvenio.getText().trim());
                
                Medico medico = (Medico)cmbMedico.getSelectedItem();
                consulta.setId_medico(medico.getIdmedico());
                Paciente paciente = (Paciente)cmbPaciente.getSelectedItem();
                consulta.setId_paciente(paciente.getIdpaciente());
                
                
                String msg;

                if(consulta.getIdconsulta() == 0){
                    msg = consultaDAO.cadastrarConsulta(consulta);
                    if(msg.equals("1"))
                        msg = "CADASTRO EFETUADO COM SUCESSO";
                    JOptionPane.showMessageDialog(rootPane, msg);
                }
                else{
                    msg = consultaDAO.atualizarConsulta(consulta);
                    if(msg.equals("1"))
                        msg = "ALTERACAO EFETUADA COM SUCESSO";
                    JOptionPane.showMessageDialog(rootPane, msg);
                }

                this.limparCampos();
                this.preencherTabela();
            };
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(rootPane, "Idade deve ser numero inteiro");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int resposta = JOptionPane.showConfirmDialog(rootPane, "DESEJA EXCLUIR O REGISTRO?");
        if(resposta == 0){
            String id = edtId.getText();
            String msg = consultaDAO.excluirConsulta(id);
            if(msg.equals("1"))
                msg = "EXCLUSAO EFETUADA COM SUCESSO";
            JOptionPane.showMessageDialog(rootPane, msg);
            this.limparCampos();
            this.preencherTabela();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

