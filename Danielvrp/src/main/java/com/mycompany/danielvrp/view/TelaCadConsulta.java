    private void preencherTabela() {
        ArrayList consultas;
        DefaultTableModel tabela = (DefaultTableModel)tblConsultas.getModel();
        tabela.setRowCount(0);
        consultas = consultaDAO.listarConsultas();
        Iterator it = consultas.iterator();
        while(it.hasNext()){
            Consulta consulta = (Consulta)it.next();
            tabela.addRow(new Object[]{ consulta.getIdconsulta(),consulta.getConvenio(),consulta.getId_medico(),consulta.getNome_medico(),consulta.getId_paciente(),consulta.getNome_paciente() });
        }
    }
    
    private void preencherComboBoxMedico() {
        ArrayList medicos;
        cmbMedico.removeAllItems();
        medicos = medicoDAO.listarMedicos();
        DefaultComboBoxModel model = new DefaultComboBoxModel(medicos.toArray(new Medico[medicos.size()]));
        cmbMedico.setModel(model);
    }
    
    private void preencherComboBoxPaciente() {
        ArrayList pacientes;
        cmbPaciente.removeAllItems();
        pacientes = pacienteDAO.listarPacientes();
        DefaultComboBoxModel model = new DefaultComboBoxModel(pacientes.toArray(new Paciente[pacientes.size()]));
        cmbPaciente.setModel(model);
    }
